package com.isb.lunarhex;

/**
 * The touch class holds information of the latest touch event.
 *
 * @author Ian Baker
 */
public class Touch
{
    /**
     * The current x position of the touch event
     */
    public static int x;

    /**
     * The current y position of the touch event
     */
    public static int y;

    /**
     * The x position of the down touch event
     */
    public static int downX;

    /**
     * The y position of the down touch event
     */
    public static int downY;
}